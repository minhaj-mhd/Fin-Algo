print(f"  Sizing: {MAX_SLOTS} slots x Rs{SLOT_CAP:,.0f}/slot x {MARGIN:.0f} leverage = "
      f"Rs{SLOT_CAP*MARGIN:,.0f} notional/trade; max book Rs{CAPITAL*MARGIN:,.0f}.")
print(f"  Signals dropped (all slots busy): {dropped}")
print(f"  NOTE: pure top-1 signal stream — no veto layers, no ATR stop. Live")
print(f"        enforce-mode (Kronos veto + candle guard + 2xATR stop) would alter fills.")
print(SEP)

